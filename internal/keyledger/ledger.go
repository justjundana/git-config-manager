// RemoveSSH removes the SSH entry with the given key path. It is a no-op if no
// such entry exists.
func (l *Ledger) RemoveSSH(keyPath string) error {
	l.mu.Lock()
	defer l.mu.Unlock()

	d, err := l.loadLocked()
	if err != nil {
		return err
	}
	filtered := d.SSH[:0]
	removed := false
	for _, e := range d.SSH {
		if e.KeyPath == keyPath {
			removed = true
			continue
		}
		filtered = append(filtered, e)
	}
	if !removed {
		return nil
	}
	d.SSH = filtered
	return l.saveLocked(d)
}

